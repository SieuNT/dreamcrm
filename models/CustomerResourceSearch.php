 */
class CustomerResourceSearch extends CustomerResource
{
    /**
     * @inheritdoc
     */
    public function rules()
    {
        return [
            [['id', 'name', 'status', 'created_by', 'updated_by', 'created_at', 'updated_at'], 'integer'],
